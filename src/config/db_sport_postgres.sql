-- =========================================================
-- VISTA: saldo de caja por sesión (JOIN corregido)
-- =========================================================
CREATE OR REPLACE VIEW v_cash_session_balance AS
SELECT
  cs.cash_session_id,
  cs.cash_register_id,
  cs.opened_at,
  cs.closed_at,
  cs.status,
  cs.opening_amount,
  COALESCE(SUM(CASE
    WHEN cm.movement_type = 'SALE_PAYMENT' THEN cm.amount
    WHEN cm.movement_type = 'CASH_IN'      THEN cm.amount
    WHEN cm.movement_type = 'CASH_OUT'     THEN -cm.amount
    WHEN cm.movement_type = 'ADJUSTMENT'   THEN cm.amount
    ELSE 0 END), 0) AS movement_net,
  (cs.opening_amount + COALESCE(SUM(CASE
    WHEN cm.movement_type = 'SALE_PAYMENT' THEN cm.amount
    WHEN cm.movement_type = 'CASH_IN'      THEN cm.amount
    WHEN cm.movement_type = 'CASH_OUT'     THEN -cm.amount
    WHEN cm.movement_type = 'ADJUSTMENT'   THEN cm.amount
    ELSE 0 END), 0)) AS expected_cash
FROM dsg_bss_cash_session cs
LEFT JOIN dsg_bss_cash_movement cm ON cm.cash_session_id = cs.cash_session_id
GROUP BY
  cs.cash_session_id, cs.cash_register_id, cs.opened_at, cs.closed_at, cs.status, cs.opening_amount;

