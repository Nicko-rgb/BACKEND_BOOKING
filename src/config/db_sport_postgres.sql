      'dsg_bss_country','dsg_bss_company','dsg_bss_business_hour','dsg_bss_person',
      'dsg_bss_client','dsg_bss_user','dsg_bss_rol','dsg_bss_claim','dsg_bss_role_claim',
      'dsg_bss_user_rol','dsg_bss_space','dsg_bss_media','dsg_bss_favorite','dsg_bss_booking',
      'dsg_bss_payment_type','dsg_bss_configuration','dsg_bss_configuration_payment',
      'dsg_bss_notification','dsg_bss_payment','dsg_bss_rating',
      -- POS / cajas / inventario
      'dsg_bss_opening_hour','dsg_bss_unit','dsg_bss_tax_rates','dsg_bss_cash_register',
      'dsg_bss_categories','dsg_bss_product','dsg_bss_product_tax_rate',
      'dsg_bss_location','dsg_bss_warehouse','dsg_bss_stock','dsg_bss_stock_movement',
      'dsg_bss_supplier','dsg_bss_purchase','dsg_bss_purchase_item',
      'dsg_bss_cash_session','dsg_bss_cash_movement','dsg_bss_sale','dsg_bss_sale_item','dsg_bss_sale_payment'
    ]) AS tbl
  LOOP
    EXECUTE format('
      DROP TRIGGER IF EXISTS trg_set_updated_at ON %I;
      CREATE TRIGGER trg_set_updated_at BEFORE UPDATE ON %I
      FOR EACH ROW EXECUTE FUNCTION set_updated_at();
    ', r.tbl, r.tbl);
  END LOOP;
END$$;

